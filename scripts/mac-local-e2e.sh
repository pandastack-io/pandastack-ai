      echo $! > "$API_PID"
    )
  fi
  for _ in {1..60}; do
    curl -fsS http://localhost:8080/healthz >/dev/null 2>&1 && break
    sleep 2
  done
  curl -fsS http://localhost:8080/healthz >/dev/null 2>&1 || die "API did not become healthy; see $LOG_DIR/api.log"
