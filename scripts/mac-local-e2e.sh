        GITHUB_APP_CLIENT_SECRET="${GITHUB_APP_CLIENT_SECRET:-}" \
        GITHUB_APP_WEBHOOK_SECRET="${GITHUB_APP_WEBHOOK_SECRET:-}" \
        PANDASTACK_DASHBOARD_URL="${PANDASTACK_DASHBOARD_URL:-http://localhost:3000}" \
